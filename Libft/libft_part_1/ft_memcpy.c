#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;
	size_t			i;

	i = 0;
	p1 = (unsigned char *) dest;
	p2 = (unsigned char *) src;
	while (i < n)
	{
		p1[i] = p2[i];
		i++;
	}
	return (dest);
}
